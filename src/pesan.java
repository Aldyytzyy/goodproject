    private int total=0;
public void autonumber(){
        try{
            Connection c = (Connection) koneksi.getKoneksi();
            Statement s = c.createStatement();
            String sql = "SELECT * FROM pesan ORDER BY id DESC";
            ResultSet r = s.executeQuery(sql);
            if (r.next()) {
                String NoID = r.getString("id").substring(2);
                String ID = "" +(Integer.parseInt(NoID)+1);
                String Zero = "";
                
                if (ID.length()==1) 
                {Zero = "00";}
                else if(ID.length()==2)
                {Zero = "0";}
                else if(ID.length()==3)
                {Zero = "";}
                
                txid.setText("0x" + Zero + ID);
            }else{
                txid.setText("0x001");
            }
            r.close();
            s.close();
        }catch(Exception e){
            System.out.println("autonumber error");
        }
    }
    
