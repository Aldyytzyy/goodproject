    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String id = txid.getText();
        String nama = namaa.getText();
        String barang = (String) barangg.getSelectedItem();
        String jumlahStr = jumlahh.getText(); // Menggunakan nama variabel yang berbeda untuk membedakan antara string dan integer
        int jumlah = Integer.parseInt(jumlahStr); // Mengonversi jumlah dari string ke integer
        int total = 0;

    
    
    if(barang.equals("Pensil")) {
        total = jumlah * 3500; 
    } else if(barang.equals("Penggaris")) {
        total = jumlah * 5000; 
    } else if(barang.equals("Busur")) {
        total = jumlah * 3000;
    } else {
        // Barang lain atau tidak dikenali, total tetap 0
        JOptionPane.showMessageDialog(null, "Barang tidak dikenali!");
    }



    try{
        Statement statement = (Statement) koneksi.getKoneksi().createStatement();
        statement.executeUpdate("insert into pesan VALUES ('" + id + "','" + nama +"','" + barang +"','" + jumlah +"'," + total +");");
        statement.close();
        JOptionPane.showMessageDialog(null, "Data Berhasil disimpan");
    }catch (Exception t){
        JOptionPane.showMessageDialog(null, "Data Gagal Disimpan");
    }
    
        
    }//GEN-LAST:event_btnSimpanActionPerformed

