    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if(jButton1.getText()== "jButton1"){
            txid.requestFocus();
            
        } else{
            String id = txid.getText();
            String nama = namaa.getText();
            String barang = (String) barangg.getSelectedItem();
            String jumlahStr = jumlahh.getText(); // Menggunakan nama variabel yang berbeda untuk membedakan antara string dan integer
            int jumlah = Integer.parseInt(jumlahStr); // Mengonversi jumlah dari string ke integer
            int total=0;
            if(barang.equals("Pensil")) {
        total = jumlah * 3500; // Jika barang adalah "Pensil", total dihitung dengan jumlah * 10
    } else if(barang.equals("Penggaris")) {
        total = jumlah * 5000; // Jika barang adalah "Penggaris", total dihitung dengan jumlah * 20
    } else if(barang.equals("Busur")) {
        total = jumlah * 3000; // Jika barang adalah "Busur", total dihitung dengan jumlah * 30
    } else {
        // Barang lain atau tidak dikenali, total tetap 0
        JOptionPane.showMessageDialog(null, "Barang tidak dikenali!");
    }
            try { 
    // Update data ke database
    Statement statement = koneksi.getKoneksi().createStatement();
    String query = "UPDATE pesan SET "
                 + "nama='" + nama + "', "
                 + "barang='" + barang + "', "
                 + "jumlah='" + jumlah + "', "
                 + "total=" + total + " "
                 + "WHERE id='" + id + "'";
    int rowsAffected = statement.executeUpdate(query);
    
    if (rowsAffected > 0) {
        JOptionPane.showMessageDialog(null, "Data berhasil diupdate");
    } else {
        JOptionPane.showMessageDialog(null, "Data gagal diupdate");
    }
    
    // Perbarui tampilan tabel
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, "Terjadi kesalahan: " + e.getMessage());
}

        }
    }//GEN-LAST:event_jButton1ActionPerformed

